export function getDocumentBlueprint(session: SessionProfile) {
  const blueprint = blueprints[session.taskType] ?? defaultBlueprint;
  return [
    `Цель документа: ${blueprint.purpose}`,
    "",
    "Обязательная структура:",
    ...blueprint.structure.map((item) => `- ${item}`),
    "",
    "Полезная инфографика внутри результата:",
    ...blueprint.visuals.map((item) => `- ${item}`),
    "",
    "Запрещено:",
    ...blueprint.reject.map((item) => `- ${item}`),
  ].join("\n");
}
